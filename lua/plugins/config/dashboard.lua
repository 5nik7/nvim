vim.api.nvim_create_autocmd("User", {
  pattern = "LazyVimStarted",
  callback = function()
    local stats = require("lazy").stats()
    local ms = (math.floor(stats.startuptime * 100 + 0.5) / 100)
    dashboard.setup({
      theme = "doom",
      hide = {
        statusline = 0,
        tabline = 0,
        winbar = 0,
      },
      config = {
        header = vim.split(
          string.rep("\n", 15) ..
          days_of_week[current_day] .. "\n" .. os.date("%d-%m-%Y %H:%M:%S" .. "\n\n\n"),
          "\n"
        ),
        center = {
          {
            icon = "   ",
            icon_hl = "DashboardIcon",
            desc = "Recent Files      ",
            desc_hi = "DashboardDesc",
            key = "r",
            key_format = '  %s',
            key_hl = "DashboardKey",
            action = "Telescope oldfiles",
          },
          {
            icon = "   ",
            icon_hl = "DashboardIcon",
            desc = "Last Session",
            desc_hi = "DashboardDesc",
            key = "s",
            key_format = '  %s',
            key_hl = "DashboardKey",
            action = "lua require('persistence').load({last = true})",
          },
          {
            icon = "   ",
            icon_hl = "DashboardIcon",
            desc = "Configuration",
            desc_hi = "DashboardDesc",
            key = "c",
            key_format = '  %s',
            key_hl = "DashboardKey",
            action = "edit $MYVIMRC",
          },
          {
